    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if (!fieldCodigoLibro.getText().isEmpty() && !fieldCantidadCopiasNuevas.getText().isEmpty()) {
            if (bibliotecario.agregarCopias(libros, fieldCodigoLibro.getText(), fieldCantidadCopiasNuevas.getText())) {
                String mensaje = "Se han agregado exitosamente " + fieldCantidadCopiasNuevas.getText() + " copia(s) nueva(s) al libro con el c√≥digo " + fieldCodigoLibro.getText();
                JOptionPane.showMessageDialog(null, mensaje);
                fieldCodigoLibro.setText("");
                fieldCantidadCopiasNuevas.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Complete los campos requeridos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed


